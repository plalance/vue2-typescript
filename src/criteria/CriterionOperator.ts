/**
 * Criterion operator
 */
export enum CriterionOperator {
    /** Equals */
    EQUALS = "=",

    /** Greater */
    GREATER = ">",

    /** Greater or equals */
    GREATER_EQUALS = ">=",

    /** Less */
    LESS = "<",

    /** Less or equals */
    LESS_EQUALS = "<=",

    /** Like */
    LIKE = "like",

    /** is null */
    IS_NULL = "is null",

    /** In */
    IN = "in"
}
